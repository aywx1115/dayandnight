package org.dayandnight.model;

public class Menu {
    public Menu(){}
    private long id;
    private String name;
    private String description;
    public void setId(long id){
        this.id=id;
    }
    public void setName(String name){
        this.name=name;
    }
    public void setDescription(String description){
        this.description=description;
    }
}
